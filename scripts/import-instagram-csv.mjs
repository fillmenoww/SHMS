// ─────────────────────────────────────────────────────────────
//  IMPORT INSTAGRAM PROFILES FROM CSV
//
//  Usage:
//    node scripts/import-instagram-csv.mjs path/to/your-file.csv
//
//  Reads your sheet (skips the header row), maps columns by name,
//  and writes src/data/igProfiles.ts — ready to use on the
//  Profiles page's Instagram section.
//
//  Expected headers (case-insensitive):
//    full_name, username
//
//  If your file is .xlsx, open it in Excel/Sheets and "Save As" /
//  "Export" → CSV first, then point this script at the .csv file.
// ─────────────────────────────────────────────────────────────
import fs from 'node:fs';
import path from 'node:path';

const inputPath = process.argv[2];
if (!inputPath) {
  console.error('Usage: node scripts/import-instagram-csv.mjs path/to/file.csv');
  process.exit(1);
}

const raw = fs.readFileSync(inputPath, 'utf8');

// Same quote-aware CSV parser as the X importer — handles commas
// and quotes inside a field correctly instead of just splitting on ','.
function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = '';
  let inQuotes = false;
  text = text.replace(/\r\n/g, '\n');

  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else {
        field += c;
      }
    } else if (c === '"') {
      inQuotes = true;
    } else if (c === ',') {
      row.push(field); field = '';
    } else if (c === '\n') {
      row.push(field); field = '';
      rows.push(row); row = [];
    } else {
      field += c;
    }
  }
  if (field.length > 0 || row.length > 0) { row.push(field); rows.push(row); }
  return rows.filter((r) => r.some((cell) => cell.trim() !== ''));
}

const rows = parseCsv(raw);
if (rows.length < 2) {
  console.error('No data rows found (is the file empty, or just a header?).');
  process.exit(1);
}

const header = rows[0].map((h) => h.trim().toLowerCase());
const dataRows = rows.slice(1); // skip row 1 (titles)

function col(headerNames) {
  for (const name of headerNames) {
    const idx = header.indexOf(name.toLowerCase());
    if (idx !== -1) return idx;
  }
  return -1;
}

const idx = {
  name: col(['full_name', 'full name', 'name']),
  username: col(['username', 'handle']),
};

const missing = Object.entries(idx).filter(([, v]) => v === -1).map(([k]) => k);
if (missing.length) {
  console.error('Could not find these columns in row 1:', missing.join(', '));
  console.error('Found headers were:', rows[0].join(' | '));
  process.exit(1);
}

function escape(s) {
  return (s ?? '').replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\n/g, ' ').trim();
}
function cleanHandle(u) {
  return (u ?? '').trim().replace(/^@/, '');
}

const profiles = dataRows
  .map((r) => ({
    name: (r[idx.name] ?? '').trim(),
    handle: cleanHandle(r[idx.username]),
  }))
  .filter((p) => p.handle); // skip blank rows

profiles.sort((a, b) => a.name.localeCompare(b.name));

const body = profiles
  .map(
    (p) => `  {
    name: '${escape(p.name)}',
    handle: '${escape(p.handle)}',
    profileUrl: 'https://instagram.com/${escape(p.handle)}',
  },`
  )
  .join('\n');

const output = `// ─────────────────────────────────────────────────────────────
//  AUTO-GENERATED by scripts/import-instagram-csv.mjs — do not hand-edit.
//  Re-run the script to regenerate this file from your sheet.
//  Generated: ${new Date().toISOString()}
// ─────────────────────────────────────────────────────────────
export type IgProfile = {
  name: string;
  handle: string;
  profileUrl: string;
};

export const igProfiles: IgProfile[] = [
${body}
];
`;

const outPath = path.join(process.cwd(), 'src/data/igProfiles.ts');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, output, 'utf8');

console.log(`Wrote ${profiles.length} profile(s) to src/data/igProfiles.ts`);
